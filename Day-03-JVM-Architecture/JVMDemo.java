public class JVMDemo {

    public static void main(String[] args) {

        System.out.println("===== JVM Execution Flow =====");

        System.out.println("Step 1 : Write Java Source Code (.java)");
        System.out.println("Step 2 : Compile using javac");
        System.out.println("Step 3 : Generate Bytecode (.class)");
        System.out.println("Step 4 : Class Loader loads the class");
        System.out.println("Step 5 : Bytecode Verifier checks the code");
        System.out.println("Step 6 : Execution Engine executes the bytecode");
        System.out.println("Step 7 : JIT Compiler improves performance");
        System.out.println("Step 8 : Garbage Collector removes unused objects");
        System.out.println("Step 9 : Output is displayed");

        System.out.println("\nJVM allows Java programs to run on any platform.");
    }
}
